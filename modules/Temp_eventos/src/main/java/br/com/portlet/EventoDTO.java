package br.com.portlet;



public class EventoDTO {
		
	private String tituloevento;
	
	private String imagemevento;
	
	private String datahorainicio;
	
	private String datahorafinal;
	
	private String descricao;
	
	private String conteudoevento;
	
	private String localidade;
	
	private String urlamigavel;

	public String getTituloevento() {
		return tituloevento;
	}

	public void setTituloevento(String tituloevento) {
		this.tituloevento = tituloevento;
	}

	public String getImagemevento() {
		return imagemevento;
	}

	public void setImagemevento(String imagemevento) {
		this.imagemevento = imagemevento;
	}

	public String getDatahorainicio() {
		return datahorainicio;
	}

	public void setDatahorainicio(String datahorainicio) {
		this.datahorainicio = datahorainicio;
	}

	public String getDatahorafinal() {
		return datahorafinal;
	}

	public void setDatahorafinal(String datahorafinal) {
		this.datahorafinal = datahorafinal;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

	public String getConteudoevento() {
		return conteudoevento;
	}

	public void setConteudoevento(String conteudoevento) {
		this.conteudoevento = conteudoevento;
	}

	public String getLocalidade() {
		return localidade;
	}

	public void setLocalidade(String localidade) {
		this.localidade = localidade;
	}

	public String getUrlamigavel() {
		return urlamigavel;
	}

	public void setUrlamigavel(String urlamigavel) {
		this.urlamigavel = urlamigavel;
	}
	
	

}

